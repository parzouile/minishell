#include "libft.h"

int	next_quote(const char *s, int i)
{
	char	g;

	i++;
	if (s[i - 1] == '\'' || s[i - 1] == '\"')
	{
		g = s[i - 1];
		while (s[i] && s[i] != g)
			i++;
		i++;
	}
	return (i);
}
